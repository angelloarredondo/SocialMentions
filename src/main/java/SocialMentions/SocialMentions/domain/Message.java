package SocialMentions.SocialMentions.domain;

public class Message {
    private String value;

    public Message(String value) {
        this.value = value;
    }

    public String getValue() {
        return value;
    }

    public String get() {
        return value;
    }

    public void set(String value) {
        this.value = value;
    }
}
